<?php define("TO_ROOT", "../../");

require_once TO_ROOT. "/system/core.php";

$data = HCStudio\Util::getHeadersForWebService();

$UserSupport = new DummieTrading\UserSupport;

if($UserSupport->logged === true)
{
    $data['demo'] = filter_var($data['demo'] ?? 1, FILTER_VALIDATE_BOOL);

    if($users = (new DummieTrading\UserTradingAccount)->getAllWithGains($data['status'],$data['demo']))
    {
        $data["users"] = $users;
        $data["s"] = 1;
        $data["r"] = "DATA_OK";
    } else {
        $data["s"] = 0;
        $data["r"] = "NOT_ACCOUNTS";
    }
} else {
	$data["s"] = 0;
	$data["r"] = "NOT_FIELD_SESSION_DATA";
}

echo json_encode(HCStudio\Util::compressDataForPhone($data)); 